						 <p class="lead" style="text-align:justify;">Mi mégis folyton a másikkal foglalkozunk. Őt akarjuk megnevelni, de minimum vágyunk arra, hogy az illető megnevelje saját magát, és ha lehet, minél hamarabb. És itt most nem csupán a közeli hozzátartozóinkra gondolok, hanem minden emberre, akivel találkozunk, sőt, minden helyzetre, ami kialakul körülöttünk. Mindenre képesek lennénk azért, hogy a külvilág megváltozzon, hogy pontosan olyanná váljon, amilyet mi szeretnénk. </p>
