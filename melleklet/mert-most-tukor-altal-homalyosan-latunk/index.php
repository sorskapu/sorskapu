						 <p class="lead" style="text-align:justify;">Egészen addig tart ez a feszültség, amíg rá nem jövünk, hogy nem megváltoztatni, hanem elfogadni kell saját magunkat. Tisztában kell lennünk jellemünkkel, személyiségünkkel, fel kell fedeznünk titkos mozgatórugóinkat, látnunk kell saját hiányosságainkat, gyengeségeinket. Majd ahelyett, hogy elfojtanánk - hiszen ezzel csak azt érnénk el, hogy mások lesznek velünk szemben olyanok! -, meg kell tanulnunk bánni velük. Arra kell törekednünk, hogy minél előbb tudatosuljon bennünk a jelenlétük. Mondok egy példát: amint észrevesszük, hogy lelkünkben felszínre tör a dacos, sértődős gyermek, azonnal képesek leszünk megnyugtatni, még mielőtt őrült hisztériázásba kezd. </p>
