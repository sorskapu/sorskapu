                         <p class="lead" style="text-align:justify;">Szóval, miközben gubbasztok ott középen, összehúzva magam, egészen kicsire, azt gondolom, megállt a világ. Meg vagyok győződve róla, hogy nincs más dolgom, mint megtartani mindazt, ami körülvesz engem. És megtorpanok. Fedezékbe vonulok. Nem megyek tovább, nem! Megállok! De lehetséges ez? Megállhatok én ott, ahol minden mozog? Megdermedhetek ott, ahol minden folyamatosan változik? Hirtelen egészen egyértelműen látom, hogy nem!</p>
                         <p class="lead" style="text-align:justify;">Engedem hát... Jöjjön, ami jön! Elfogadok mindent. Meztelen vagyok és védtelen, mint egy újszülött, akit kisodor az ár a nagyvilágba. És ez így van jól.</p>
                         <p class="lead" style="text-align:justify;">S már érzem is a talajt a lábam alatt. Elképzelem, hogy erős vagyok és bátor. Én, egymagam. Aki tulajdonképpen mindent és mindenkit birtokol, hiszen én Ti vagyok, én minden vagyok, én vagyok az egész Világegyetem!</p>
                         <p class="lead" style="text-align:justify;">Úgyhogy nem ülök itt tovább, létező vagy nem létező burokba bújva, hanem belevetem magam az ismeretlenbe! Bátran, mintha nem csupán egy ember lennék, aki csak szeretetre, melegségre és megértésre vágyik, hanem egy igazi hős! Egy hős, aki saját maga megmentésére indul, hogy felépíthesse végre saját várát: Önmagát. Ha a vár kapuján láttok majd egy táblát, amin ez lesz olvasható: "Istenem, köszönöm, végre szeretem magam! Szavakba nem önthető, micsoda megkönnyebbülés ez!", akkor tudni fogjátok, hogy ott lakom én, a legnagyobb biztonságban.</p>
