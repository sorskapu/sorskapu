                         <p class="lead" style="text-align:justify;">De hol van itt a biztonság? Mi lesz így velem? Egyedül! Villámok, idegen sugárzások, üstökösök, lávatörmelék és árvíz... mind-mind veszélyt jelentenek számomra! Sürgősen építenem kell valami védőfalat, egy bástyát! Akár áshatok egy árkot is, ami körülvesz engem, csak ne legyek védtelen az engem bombázó, fájdalmat okozó, idegen erőkkel szemben! Kell valami, amit birtokolhatok, ami csak az enyém, ami jelzi a világnak: hé, én értékes ember vagyok, ne közelíts, ne bánts! Látod, milyen szép házam van? Látod, milyen autóm, micsoda könyvtáram, ez meg az, látod? Ez mind én vagyok! Én ilyen nagy vagyok! Látod, micsoda eszméim vannak? Látod, micsoda tudás vesz körül?</p>
                         <p class="lead" style="text-align:justify;">Tiszta és világmegváltó gondolataimból bűvös kört építettem magam köré, és titokban ott kuporgok a gyújtópontban, értéktelennek érezvén magam, s tudván, hogy valójában semmim sincs. De még ragaszkodom! Még védekezem! Holott már sejtem, egyszer újra megreped a burok. És akkor jönnek majd! Bekúszik hozzám újra a veszélyes nagyvilág... Elveszítek mindent. Az árvíz lerombolja a házamat. Minden odavész, amit vettem, készítettem, amiért megdolgoztam! Semmim sem marad... A tárgyak elúsznak, az eszméimet felemészti a tűz, s kicsúszik a talaj a lábam alól. Nem kapok levegőt... Megfulladok! Istenem, segíts! Miért nem lehetek még mindig ott, a burokban? Ott még lélegeznem sem kellett!</p>
                         <p class="lead" style="text-align:justify;">Megpróbálok még valamit. Keresek valakit magam mellé. Egy másik embert, akit szerethetek. Aki majd az én Drágám lesz! Igen, ez az! Most majd ő lesz az enyém! Mostantól csakis hozzá ragaszkodom majd! És akkor újra biztonságban érezhetem magam!</p>
                         <p class="lead" style="text-align:justify;">De nem! Ez sem sikerült! Ez sem az... Féltékeny vagyok! A védőbástyámnak megint megrepedt a fala! Ez nem lehet igaz! Megint beszabadult valaki! Itt a veszély, itt egy idegen újra! Mi lesz most velem?!</p>
