						 <p class="lead" style="text-align:justify;">Biztonságra vágyom. Miért? És mi is az tulajdonképpen? Egy emlék. Egy kiragadott részlet az életemből. Amikor még édesanyám testében-lelkében voltam és körülölelt valami puha melegség, ami állandónak tűnt. Fel sem merült bennem, hogy egyszer csak megreped a burok, hiszen nem is tudtam a létezéséről.</p>
