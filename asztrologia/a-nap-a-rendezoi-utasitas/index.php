				    <div class="panel panel-default">
					     <div class="panel-heading" style="text-align:center;"><h2>A Nap - a rendezői utasítás</h2></div>
					     <div class="panel-body">
						 <br />
                         <p class="lead" style="text-align:justify;">A Nap állása a horoszkóp legismertebb eleme. Ha valaki sohasem foglalkozott asztrológiával, vagy éppenséggel az asztrológia ellenzői közé tartozik, még az is tudja, hogy ő Kos, mert március 25-én született.</p>
                         <p class="lead" style="text-align:justify;">Az ember úgy viselkedik, ahogyan azt a rendezői utasítás – azaz a Nap állása – előírja. A házakban (az élet tájain, a színpadon) megtalálja azokat a körülményeket, amelyek egy adott feladattal vagy témával való szembenézésre kényszerítik. A Nap állása mutatja meg, hogyan viselkedik az ember az adott körülmények között.</p>
                         <p class="lead" style="text-align:justify;">Az egója révén minden ember konfliktusba kerül a külvilággal és más emberek egójával. Ezek a konfliktusok a horoszkópban a Napnak a házakhoz viszonyított helyzetéből, valamint a Nap más bolygókkal bezárt fényszögeiből olvashatóak ki.</p>
                         <p class="lead" style="text-align:justify;">Az egóval kapcsolatos konfliktusok a legtöbb ember esetében azonos módon folynak le, és alapvetően két nagy csoportba oszthatók: az egyik lehetőség, hogy az ego túl nagy, túl erős, túllő a célon, és ezért a külvilág korrigálja a hibát. A másik, hogy az ego túl kicsi és gyenge, és addig szenved ettől, amíg meg nem tanulja elfogadni önmagát.</p>
                         <p class="lead" style="text-align:justify;">Az ember összeütközései és problémái az egójával nem tartoznak az árnyékhoz, de az árnyék vezeti oda az egót. Ha nem súrlódnánk a külvilággal, ha az egónk nem emelkedne és süllyedne hullámszerűen, elégedetten élnénk a világunkat az önmagunkról alkotott képünk alapján. Senki sem tenné fel a kérdéseket, senki sem keresne válaszokat. Emiatt van szükség arra, hogy konfliktusba kerüljünk az egónkkal, hogy elveszítsük a biztonságérzetet, hogy a lelkünk nyugtalanul hánykolódjék. Csak így tanulunk meg tapasztalatokat szerezni. A nagy rendező színpadi viselkedésre vonatkozó utasításainak mindig része a kudarc és a csapda is. Az ember csak akkor veszíti el a naivitását (a körülmények hatására), és csak akkor adja fel a saját tökéletességébe vetett hitét, ha beleesik egója csapdájába. Csak akkor hajlandó önmagába nézni, ha a külvilágban már nincs más megoldási lehetőség.</p>
                         <p class="lead" style="text-align:justify;"><b>Nap a Kosban:</b><br />Az utadba eső terület nagy részét már letaroltad, és hirtelen nincs több feladat. Nincs új kezdet, nincs ellenség, nincs harc, nincs kihívás a láthatáron. Te viszont csak egy szerepet tudsz játszani. Az élet pedig hirtelen olyan színdarabot ír, amelyben nincs harcos, vagy esetleg valaki más játssza a szerepet. Az egód megsérül. Nem tud mit kezdeni az ürességgel. Ki vagy, ha nincs feladatod? Ki vagy, ha nem harcolhatsz szenvedélyesen valakiért vagy valaki ellen? Ki vagy?</p>
						 <p class="lead" style="text-align:justify;"><b>Nap a Bikában:</b><br />A sors pontosan ott húzza meg az emelőkart, ahol a legtöbb a rejtegetnivaló, és ahol a legsötétebb árnyék rejtőzik. Önmagadtól semmi sem ösztönöz arra, hogy benézz a meleg ágy alá is, és a sors emiatt kénytelen lerántani a földre, hogy meglásd, mi rejtőzik ott lenn. Azért, hogy megnézd, ki vagy. Ki vagy, ha nem véd meg mindaz, amit magad köré gyűjtöttél? Ki vagy, ha a barátaid nem igazolják a létezésedet? Ki vagy, ha már nem a tiéd a múltad? Ki vagy?</p>
                         <p class="lead" style="text-align:justify;"><b>Nap a Ikrekben:</b><br />Megszoktad, hogy a külvilágban vezeted le energiáidat, egyik helyről a másikra mész, ezért nem tudsz magaddal mit kezdeni, ha a sors lezárja előtted az utat. Akkor kénytelen vagy önmagadnak feltenni azokat a kérdéseket, amelyeket addig általában a világban tettél fel, és akkor az a feladatod, hogy belépj önmagad ismeretlen tájaira. Ki vagy, ha nem áradnak feléd az információk? Ki vagy, ha nem lehetsz mindig úton, és ha sehová sem érkezel meg? Ki vagy?</p>
                         <p class="lead" style="text-align:justify;"><b>Nap a Rákban:</b><br />Miért éppen te, amikor neked van a legnagyobb szükséged az emberi közelségre és a védelemre, mert egyedül lényegében életképtelen vagy. Pontosan azért, mert te csak végső esetben, sebesülten és üldözve hagyod el a fészkedet, hogy valóban elindulj megkeresni önmagadat és a lelkedet. El kell veszítened önmagadat másokban, hogy ismét visszatalálj saját magadhoz. A környezetednek az a feladata, hogy elutasítson és eltaszítson, hogy aztán fájdalommal telve feltedd a kérdést: “Ki vagyok én valójában?”</p>
                         <p class="lead" style="text-align:justify;"><b>Nap a Oroszlánban:</b><br />Ha az ember nagyon magasról zuhan le, akkor az nagyon fáj. A válsághelyzetek, amelyek elgondolkodásra kényszerítenek, mindig a kudarccal és a vereséggel vannak összefüggésben. Csak akkor kezdesz felnőni, ha elkezded a léted hátterét kutatni, és ha már nem vagy annyira biztos önmagadban. Kérdezz tehát, és ébredj fel! Ki vagy valójában, a belőled sugárzó ragyogó Nap mögött? Ki vagy?</p>
                         <p class="lead" style="text-align:justify;"><b>Nap a Szűzben:</b><br />Nyugtalanul hánykolódsz, újra és újra elemzed a helyzetet, miközben egyre gyengébbé válsz. Nem működnek az általad kiépített elhárító mechanizmusok, ki vagy szolgáltatva a magasabb hatalmaknak. Félelemmel telve kérdezed magadtól: “Miért történik mindez, és miért pont velem?” Ki vagy, ha csődöt mond a védelmi rendszered? Ki vagy, ha a logikád nem tudja megválaszolni a kérdéseket? Ki vagy, ha a leggondosabb előkészületekkel sem tudod elkerülni a káoszt? Ha olyan erőknek vagy kiszolgáltatva, amelyek felett nincs hatalmad? Ki vagy?</p>
                         <p class="lead" style="text-align:justify;"><b>Nap a Mérlegben:</b><br />Kapcsolataid hálója laza és levegős, te pedig könnyedén táncolsz a szálakon. De mi történik, ha ellentmondásokba keveredsz, ha már nem lehetséges, hogy továbbra is kihúzd magad a kötelezettségek alól, és ha a sors fogságába kerülsz? Ha döntened kell? Ha válaszút előtt állsz, és képtelen vagy cselekedni? Bénultan és tehetetlenül omlik össze az egykor olyan rugalmas egód. Ki vagy, ha a szellemi rugalmasságod, amely nagy mozgásszabadságot élvezett, egyszerre csak megmerevedik, és már nem tudod azonosítani magad másokkal? Ki vagy?</p>
                         <p class="lead" style="text-align:justify;"><b>Nap a Skorpióban:</b><br />A táj szürkesége elnyelte a régi ideálokat. Nem tudsz tájékozódni, úgy érzed, hogy becsapott a sors, és elhagyott ez élet. Semmi sem mozdul a bensődben, és attól félsz, hogy elmerülsz egy nagy fekete lyukban. Mindent elvesztettél, te magad is elvesztél; csak most tudod feltenni magadnak a kérdést önmagadról. Ki vagy, ha már nem lehetsz a magadról alkotott elképzelés? Ki vagy tulajdonképpen?</p>
                         <p class="lead" style="text-align:justify;"><b>Nap a Nyilasban:</b><br />A tudásra és ismeretekre törekvő hajtóerő nálad csak egyetlen irányt ismer: előre! A hegyről tehát nem tudsz lemászni. A veled együtt haladók, anélkül, hogy észrevetted volna, kiestek az általad megteremtett szellemi rezgésből. Ezért életed legválságosabb pillanatában egyedül állsz a Tudás hatalmas hegyén, és csak most jössz rá, hogy nem az vagy, akinek gondoltad magad. Most jött el az idő, hogy visszaemlékezz arra, ki vagy, és hogy nem körülötted forog a világ. Ki vagy te valójában?</p>
                         <p class="lead" style="text-align:justify;"><b>Nap a Bakban:</b><br />Az egód csak akkor életképes, ha szilárd talajt érez a lába alatt, és ha a már megszerzett tapasztalatai alapján dönthet. Ki vagy szolgáltatva mindennek, ami új, ami előre nem látható, és hirtelen tör be az életedbe. A káosz látványára pánikkal reagálsz. Hol maradt a megfontolt nyugalmad? Nem ismersz önmagadra. Ki vagy, ha leomlanak a szabályaidból felépített falak? Ki vagy, ha a kozmikus szél elfújja logikádat és gyakorlatiasságodat? Ki vagy?</p>
                         <p class="lead" style="text-align:justify;"><b>Nap a Vízöntőben:</b><br />Megszűnsz létezni, ha fogságban vagy és leláncolnak. Ha nem tudsz repülni, nem tudsz – és nem is akarsz – élni. Csak akkor tudod meglátni magadat a világban, ha a magasból tudsz lenézni rá. És csak ilyenkor merül fel az a kérdés, hogy ki is vagy valójában. Tudod, hogy ki vagy, ha erőteljes szárnycsapásokkal a levegőbe emelkedsz. De ki vagy a Földön? Ki vagy?</p>
                         <p class="lead" style="text-align:justify;"><b>Nap a Halakban:</b><br />Te vagy az egyetlen ego a tizenkettő közül, aki állandóan azt kérdezi magától, hogy ő kicsoda. A válaszok olyan homályosak és kétértelműek, mint te magad. Amíg azonban keresel, az egyik kérdést a másik után teszed fel, addig az életenergiád erősen és egészségesen áramlik. Ha azonban megpróbálsz letelepedni az egyik parton, ha határokat húzol, ha megpróbálod leküzdeni életidegenségedet, akkor problémákkal szembesülsz. Akkor már nem önmagad vagy. De ki is vagy?</p>
