						 <br /> 
                         <p class="lead" style="text-align:justify;">Azt nevezzük teliholdnak, amikor a Nap és a Hold szemben állnak az égen. Azaz két teljesen eltérő energia folytat küzdelmet egymással a lelkünkben, s ez bizony okozhat némi feszültséget. </p>
                         <p class="lead" style="text-align:justify;">Érdekes módon az oppozíció (180 fokos fényszög, szembenállás) belső tartalmát éppen a Hold és a Nap szembenállása jelképezi. Tehát minden más hasonló helyzetben álló bolygó találkozásánál jelen van ez az energia. Képzeljük el, hogy nem bolygókról, hanem emberekről beszélünk! Egy férfi és egy nő egymással farkasszemet nézve áll. A küzdelem lényege az, hogy kiderüljön, melyikük az erősebb, azaz melyikük képes a másikat kibillenteni a helyzetéből, s maga mellé állítani, hogy végre ugyanabba az irányba nézzen, mint ő. Mind a ketten másfelé tartanak ugyanis, s jelenleg egymás útjában állnak. A kártyán látható kép nagyon szépen ábrázolja ezt a témát: hiába mennék én erre (a nő az érzelmek, az otthon, a lélek felé), s te arra (a férfi a cselekvés, az uralkodás, a hatalom gyakorlása felé), nem létezhetünk egymás nélkül, szükségünk van egymásra; de az egyensúlyt csak úgy hozhatjuk létre, ha mindenki marad a saját útján, s nem kényszeríti a másikat arra, hogy ő is azon járjon. </p>
                         <p class="lead" style="text-align:justify;">Tapasztalatom szerint azoknál az embereknél jelentkezik a legnagyobb feszültség telihold idején, akiknél ez a két energia nincs egyensúlyban önmagukon belül. Hiszen mindannyiunkban ott van a férfi is és a nő is, csupán más-más arányban. Természetesen a többi bolygó szembenállásakor is felmerülhet náluk ez a téma (s valószínűleg ezt a saját születési képletük is jelzi), éppen azért, mert megoldásra vár. Teliholdkor azonban hirtelen, gyorsan és intenzíven mutatja meg magát. </p>
                         <p class="lead" style="text-align:justify;">Az a nő, akit folyamatosan elárasztanak az érzelmek, s hiányzik belőle a cselekvőképességhez szükséges erő, ilyenkor erőszakossá válhat. Megijed, amikor érzi (hiszen ezt is nagyon jól érzi!), hogy valami hiányzik belőle, s arra kényszeríti a másikat, hogy álljon mellé, adja meg neki azt, amitől biztonságban érezhetné magát. Hiányosságát nem építő jellegű férfi energiával, hanem annak egy pusztító változatával pótolja: agresszív és erőszakos lesz. </p>
                         <p class="lead" style="text-align:justify;">Az a férfi, aki végtelen mennyiségű cselekvőerővel rendelkezik, de nem érzi otthon magát a világban, ilyenkor dacossá, sértődékennyé válhat. Megijed, amikor azt tapasztalja, hogy nem tud olyan gyorsan és határozottan haladni célja felé, mint ahogy szeretne. Úgy érzi, a másik akadályozza, lassítja őt. Hiányosságát nem építő jellegű női energiával, hanem sértődéssel, bezárkózással, túlérzékeny reakciókkal pótolja.</p>
                         <p class="lead" style="text-align:justify;">Hálásak lehetünk tehát a teliholdnak (ahelyett, hogy félnénk tőle), mert azt üzeni nekünk: fordítsunk több figyelmet arra az oldalra, amit eddig elhanyagoltunk. Vessünk véget az egyoldalúságnak: női oldalunkhoz képzeljük hozzá férfi párját, s fordítva!</p>
