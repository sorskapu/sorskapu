                         <p class="lead" style="text-align:justify;">Mivel most a Ikrekben áll a Hold, számíthatunk arra, hogy az érzelmeink és az értelmünk viaskodni kezdenek egymással. Állandóan változó lelkiállapotunk megnehezíti, hogy dönteni tudjunk. Amint elköteleződnénk valamelyik irányba, kételkedés lesz úrrá rajtunk, s így rögtön átbillenünk az ellenkező oldalra. Sokakban felvetődik a kérdés, hogy a józan eszükre hallgassanak, vagy inkább engedjenek utat az érzelmeiknek. Utóbbi esetben megvan a veszélye annak, hogy az ember, amint elhatalmasodnak rajta az érzelmek, újra függő helyzetbe kerül. </p>
                         <p class="lead" style="text-align:justify;">Ezekben a napokban értelmetlen, felszínes szócsatákba keveredhetünk, jellemző lehet a cinikus fellépés, mert mindenki meg van győződve arról, hogy ő mindent jobban tud. Többet zsörtölődünk, s igyekszünk megmagyarázni, mit miért érzünk úgy, ahogyan érzünk. Felerősödik a vitatkozásra és okoskodásra való hajlam.</p>
