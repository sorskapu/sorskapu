						<p class="lead" style="text-align:justify;"><b>Neptunusz:</b><br />A nevem sokféle lehet, mindenkinek más vagyok. Erőtlen vagyok ott, ahol te erős, de erős ott, ahol te gyenge. S hogy mit akarok? Semmi mást, csak azt, hogy figyelj rám!</p>
						<p class="lead" style="text-align:justify;"><b>Mars:</b><br />Kénytelen vagyok...</p>
						<p class="lead" style="text-align:justify;"><b>Neptunusz:</b><br />Nem kényszerítenélek, ha magadtól is észrevennél. Mostantól kezdve biztos így lesz, gyakran eszedbe fogok jutni</p>
						<p class="lead" style="text-align:justify;"><b>Mars:</b><br />Mire gondolsz?</p>
