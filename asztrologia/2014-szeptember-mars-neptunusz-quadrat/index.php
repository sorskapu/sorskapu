						<p class="lead" style="text-align:justify;"><b>Mars:</b><br />Nincs szükségem a segítségedre! Különben is, ugyan hogy tudnál te nekem segíteni? Amióta kapcsolatba kerültem veled, elhagyott minden erőm! Rossz hatással vagy rám!</p>
						<p class="lead" style="text-align:justify;"><b>Neptunusz:</b><br />Elveszem az erődet, igen. De csak azért, hogy más területekre irányítsalak, ahol aztán újra erősnek érezheted majd magad. És igenis segíthetek neked! Rendelkezésedre bocsátom az energiámat, hogy hasznosíthasd azt a saját életedben. Nemcsak az illúzió, a hazugság, a kábulat, a csalódottság, a zavarodottság, a depresszió és a kiábrándultság az, amit adhatok neked! Hanem a tisztánlátás, a kifinomultság, a magasabb szintű, spirituális gondolkodás, a mély, önzetlen, tiszta szeretet mind az én ajándékom!<br /><br />De mivel ezt nem tudjátok, néha kénytelen vagyok lazítani a földi világhoz rögzítő kötelékeiteken. Érzékennyé teszlek titeket. Azokra a betegségekre, amiket én okozok nektek, csakis bennem találhatjátok meg a gyógyszert. Láss most meg magadban te is engem! Keresd meg, melyik arcomat  éled meg belül! S ha megtaláltad, adj neki nevet! Aztán nem kell tenned semmit! A változás magától létrejön. Hiszen csakis így jöhet létre. Én nem csinálok semmit. Én ártatlan vagyok.</p>
