						<p class="lead" style="text-align:justify;">Ebben a jelenlegi esetben nagy esély van arra, hogy a Mars elfojtásra kerül, tehát a kezdeményező - és cselekvőerőnk nem feltétlenül lesz a helyzet magaslatán. Gyengeséget, erőtlenséget érezhetünk. Testünk kimerült és fáradt, nehezen tudjuk rávenni magunkat a cselekvésre. Helyette inkább álmodozunk és fantáziálunk, természetesen arról, hogy mi mindent szeretnénk csinálni.</p>
