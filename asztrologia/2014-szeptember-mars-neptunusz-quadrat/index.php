						<p class="lead" style="text-align:justify;">Mars csak állt, nézett maga elé némán, s nem igazán értette, mi történt vele az imént. Teljesen leblokkolt. Fogalma sem volt, milyen irányba induljon tovább. Megdöbbentette, amit érzett: semmiért és senkiért nem küzdött ott, abban a pillanatban. Hirtelen azt se tudta, hogy kicsoda.</p>
						<p class="lead" style="text-align:justify;">A távolban sűrű ködfelhő úszott, de úgy érezte, Neptunusz otthagyott magából egy darabot. Csak neki. Hogy megértsen valamit saját magáról.</p>
						<p class="lead" style="text-align:justify;">Mit is mondhatnék még ezután a beszélgetés után?</p>
						<p class="lead" style="text-align:justify;">Haladj lassan, lépésről lépésre! S ha tudsz, foglalkozz most a lelkeddel! Hatalmas felismerésekre tehetsz szert, aminek következtében bizonyos konfliktusok feloldódhatnak benned. Ha eluralkodik rajtad a bizonytalanság, ha elfog a menekülési vágy, gondolj arra, hogy hiába menekülnél, saját maga elől úgysem menekülhet az ember!</p>
