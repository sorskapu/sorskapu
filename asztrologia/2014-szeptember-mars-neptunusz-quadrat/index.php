						<p class="lead" style="text-align:justify;"><b>Mars:</b><br />Kezdesz idegesíteni. Sejtelmes vagy, és ez nem tetszik.</p>
						<p class="lead" style="text-align:justify;"><b>Neptunusz:</b><br />Te pedig könnyen dühbe gurulsz, ha valami nem tetszik, igaz?</p>
						<p class="lead" style="text-align:justify;"><b>Mars:</b><br />Ne sértegess, inkább áruld el végre, mit akarsz tőlem!</p>
