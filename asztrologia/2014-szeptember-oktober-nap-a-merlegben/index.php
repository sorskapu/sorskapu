				    <div class="panel panel-default">
					    <div class="panel-heading" style="text-align:center;"><h2>2014. szept. 23 - okt. 22. | Nap a Mérlegben</h2></div>
					    <div class="panel-body">
						<br />
						<p class="lead" style="text-align:justify;">Napsütéses, meleg idő várható, kellemes, lágy fuvallatok kíséretében. Éjszakánként rengeteg csillag ragyog majd az égen.</p>
						<p class="lead" style="text-align:justify;">2014. szeptember 23-án a Nap belépett a Mérleg jegyébe. 30-án csatlakozik hozzá a Vénusz is, tehát október 24-ig egymás társaságában haladnak tovább.</p> 
						<p class="lead" style="text-align:justify;">Ebben az időszakban erős igényünk támad a harmóniára, a szokásosnál jobban vágyunk a békére, ezért hajlandóak vagyunk tenni is valamit annak létrejöttéért. Szívesen fordulunk a művészetek felé, s könnyebben megtaláljuk az élet élvezetéhez vezető utat, vonz minket a szépség, s gyakran részesülünk örömteli pillanatokban. </p>
						<p class="lead" style="text-align:justify;">Mindezek mellett elsősorban kapcsolatainkra tevődik a hangsúly. Ha akarjuk, ha nem, a számunkra eddig megszokottakhoz képest most több emberrel hoz minket össze a sors, valószínűleg nem véletlenül. Minden egyes ilyen alkalommal esélyt kapunk arra, hogy megismerjük arcunk egy új oldalát. </p>
						<p class="lead" style="text-align:justify;">Igazán fontossá azok a találkozások válnak, amelyek valamilyen szinten nyomot hagynak bennünk. Ha pozitív dolog történik, boldogok vagyunk, mert a másik fényt sugároz ránk. S ebben a fényben fényesnek látjuk saját magunkat is.</p>
						<p class="lead" style="text-align:justify;">De amikor egy olyan ember lép elénk, akinek árnyékos oldalát látjuk, már nem olyan egyértelmű számunkra, hogy meglássuk saját árnyékunkat is. Pedig a feladat éppen ebben áll! Rá kell találnunk magunkban arra a helyre, ahol pontosan az az idegesítő alak él, aki a külvilágban egy másik embernek álcázza magát. Nehéz ezt felfogni; első hallásra talán nem is lehetséges.</p>
						<p class="lead" style="text-align:justify;">Hogy jobban megértsük, vonatkoztassuk most ezt az egészet egy párkapcsolatra! Beszélgessünk egy kicsit arról, amit ez a Mérleg Nap és ez a Mérleg Vénusz tanítani akar nekünk! Szereplőinknek adhatjuk a Nap és a Vénusz nevet, de lehetnek ők egymás kivetülései is, mármint a bennem élő férfi, s a benne élő nő.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Óh Istenem, annyira vágyom az ideális társra, hogy azt el se tudom mondani! Minden gondolatom Ő. Egészen pontos elképzeléseim vannak arról, hogy milyennek kell lennie. Érzem, hogy hamarosan megérkezik! Nemsokára elém lép Ő, aki pontosan olyan lesz, mint amilyet én szeretnék!</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Hallom ám, amiket mondasz! S ha nem haragszol, nem köszöntenélek most újra, hiszen itt vagyok veled, amióta megszülettél!</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Nahát, tudtam, hogy eljössz! Milyen erős vagy, milyen hatalmas és méltóságteljes, csak úgy sugárzik rólad az életöröm!</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />De hiszen mondtam már, hogy én nem most jöttem, régóta veled vagyok, nem érted? Lehet, hogy úgy tűnik, kívül vagyok, hiszen rám nézel, meg is érinthetsz, szólhatsz hozzám, de valójában benned élek, a te lelkeden belül.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Hmmm... határozottnak tűnsz. De nem igazán értem, amit mondasz. Már hogy lehetnél te az én részem, amikor folyton csak sóvárgok utánad? Amikor folyton hiányzol? </p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Pedig ott vagyok, csak nem látsz, mert nem vagy tudatában! De figyelj csak, megvilágítom magam benned, hátha akkor észreveszel! </p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Én lennék tehát az a gyönyörűséges valaki, aki előttem áll? Én vagyok az a fénylő alak, akit látok, ha rád nézek?</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Igen, te vagy! Ki más lenne? Csakis saját szépségedet tükrözhetem vissza rád! De vigyázz, még nem ismersz engem eléggé! Épp hogy összetalálkoztunk!</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Az előbb még azt mondtad, mindig is velem voltál.</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Így van! De a tudatodba csak most kezdek lassan beszivárogni. Lehetnénk akár 40 éves házasok is, a helyzet akkor is ugyanez lenne! Mivel eddig nem vetted észre, hogy én te vagyok.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Valóban nem. Ez azt jelenti, hogy mi egyformák vagyunk?</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Bizonyos szempontból igen, más szempontból nem, épp egymás ellentétei. De most tényleg arról szeretnék veled beszélgetni, amiben hasonlítunk egymásra. </p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Miért kell erről beszélgetnünk? Nem lehetne inkább egyszerűen csak élvezni egymás társaságát?</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Dehogyisnem! Mindig így indul a történet. Amíg szép lassan el nem kezdünk függeni egymástól.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Mármint egymástól tesszük függővé a saját boldogságunkat?</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Igen. Én azt fogom érezni, hogy neked kell engem boldoggá tenned.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Én pedig azt, hogy az én boldogságomért te vagy a felelős...<br />Most elgondolkodtam egy kicsit. Szomorú lenne, ha így cselekednénk.</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Pedig így lesz. Mindig így van. Nem csupán azt kapod a másiktól, amit szeretnél, hanem azt is, amire szükséged van.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Ismerek egy párt. Egyikük vak, a másik béna. A vak azt mondja, azért olyan sötét a világ, mert a társa béna. A béna pedig panaszkodik, hogy azért nem tud menni, mert a másik vak.</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Mindig a másik a hibás...</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />De akkor így mi lesz? Szakítani fogunk egymással?</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Ha képesek leszünk arra, hogy hűvösebb időkben a másiktól függetlenül is örömöt és boldogságot, elégedettséget és szerelmet, szeretetet érezzünk, akkor nem fogunk elválni egymástól.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Az én elképzeléseim szerint is kell még egy-két dolog a boldogsághoz. Például az, hogy legyen valami közös dolog az életünkben. Valami, ami mind a kettőnk számára fontos, mert akkor az össze fog tartani minket.</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Nem beszéltem még egy nagyon fontos dologról.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Figyelek.</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Minden egyes alkalommal, amikor valami olyasmit látsz bennem, ami nem tetszik neked, emlékeztetned kell magad arra, hogy te pontosan ugyanolyan vagy.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Értem. Éppen ezért háborít fel, amit látok. Ha nem lennék olyan, nem is zavarna, nem idegesítene az, hogy te olyan vagy.</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Megérteni ezt még csak nem is olyan nehéz. De véghezvinni a gyakorlatban egyáltalán nem egyszerű. Amikor épp dühös vagy rám, nem szívesen gondolsz arra, hogy végső soron magadra vagy dühös.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Hát igen... És amíg mérges vagyok, addig nem látok tisztán.</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Pontosan! Mint mindenki másnak, nekem is vannak olyan tulajdonságaim, amik nem tetszenek majd neked. Néha úgy fogok viselkedni, hogy legszívesebben a falra másznál tőlem. És eljön az idő, amikor teljesen eleged lesz belőlem ezek miatt.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Most már értem, hogy mire akarod felhívni a figyelmemet. Arra, hogy ez fordítva is így lesz. Neked is eleged lesz belőlem, mert én is sokkal színesebb vagyok annál, akit az elején megtapasztalsz.</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Igen. Tudnunk kell, hogy így lesz. Mert akkor nem ér majd meglepetés.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />De mit kezdjünk majd egymással ezek után? Csalódottságunkban keressünk magunk mellé valaki mást? Valaki olyat, aki...? De hiszen ez lehetetlen! Épp az előbb magyaráztad el, hogy ez mindenkivel így van. Előbb-utóbb benne is felfedezném azokat a dolgokat, amik elől legszívesebben elmenekülnék.</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Ráadásul hasonló dolgokat fedeznél fel benne, mint bennem! Ha nem pontosan ugyanolyanokat!</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Komolyan?</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Hát persze!</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />De miért történik mindez így?</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Azért, hogy megtanuljuk: amit a másik megmutat magából, ránk is vonatkozik.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Tehát azért találkoztam veled, hogy megtanuljam elfogadni magamban azokat a rossz tulajdonságokat, amelyeket te képviselsz számomra a világban. Minden, amit teszel, és ahogyan teszed, azért van, hogy felfedezhessem saját magamat.</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Meg kell értenünk, hogy nincs értelme haragudni, ítélkezni, megsértődni, veszekedni és kizárni magunkat egymás életéből.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Nincs értelme azzal ámítani magunkat, hogy a másik tökéletes, hiszen mi magunk sem vagyunk azok.</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Nincs értelme szakítani, hiszen a következő ideális társ éppen azokban a dolgokban fog hasonlítani rám, amelyek miatt elmenekülnél.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />És beletörődni sincs értelme. Megalkuvón nézni, hogy a másik már csak ilyen. Kiábrándultan tűrni az összes hibáját, s közben csendesen szenvedni.</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Valóban ennek sincs semmi értelme!</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Akkor maradhatunk csak boldogok egymás mellett, ha mind a ketten képesek leszünk arra, hogy a negatív, kellemetlen dolgokat magunkénak ismerjük el. Ha sosem felejtjük el, hogy én is pontosan ugyanolyan vagyok, mint a másik, a látszat hiába csal.</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Pontosan ugyanolyan.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />S amint megtanuljuk a dolgokat helyesen kezelni, minden megváltozik. Nem lesz szükség arra, hogy bizonyos megnyilvánulásaid miatt kiboruljak, hiszen tudni fogom, hogy végső soron saját magam miatt borulhatok csak ki igazán.</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />És ha ezt velem kapcsolatban már tudni fogod, akkor az is egyértelmű lesz számodra, hogy ez mindenki mással szemben is igaz. Minden egyes emberrel szemben, akivel találkozol.</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Egymás tükrei vagyunk mi, emberek. A másikban csak az zavarhat, ami ott van bennem is, valahol mélyen, legbelül.</p>
						<p class="lead" style="text-align:justify;"><b>Nap:</b><br />Úgy érzem, veled minden nagyszerű! Megértjük egymást, kitartóak és hűségesek vagyunk. Akkor is, ha még nem oldottunk fel minden konfliktust. Sőt, talán éppen ezért!</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz:</b><br />Veled megvan az esély arra, hogy egy tudatos, boldog szerelemben éljek! Bármelyik pillanatban felismerhetem ezt, és ez csodálatos! Minden...</p>
						<p class="lead" style="text-align:justify;"><b>Vénusz és Nap egyszerre:</b><br />... csak rajtam múlik!</p>
						<p class="lead" style="text-align:justify;"><b>Hold:</b><br />Minden csak rajtatok múlik! Én nagyon szeretlek titeket! És örülök, hogy ilyen erősek vagytok és bátrak! Szükségem van rátok! Szeressétek egymást amíg világ a világ! S éljünk együtt boldogan, amíg csak lehet! Én örökké hálás leszek ezért! Az Isten velünk van, s azt üzeni: ne féljetek!</p>
