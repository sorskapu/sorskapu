					     <div class="panel-heading" style="text-align:center;"><h2>2012 - 2015. | Uránusz - Plútó quadrát</h2></div>
					     <div class="panel-body">
						 <br />
						 <p class="lead" style="text-align:justify;">Uránusz szerepe az volt a mitológiában, hogy elszakítsa a kötelékeket; elválasztott és feloldott. Megszüntetett mindent, ami idejétmúlt és öreg volt; mindent, ami nem létezhetett tovább az addigi alakjában.</p>
                         <p class="lead" style="text-align:justify;">Az Uránusz lezárja a különböző fejlődési szakaszokat, és előkészíti a belépést az új szakaszba. Kiemel a munkából (felmondás), a kapcsolatainkból (szakítás), esetleg a lakóhelyünkről (elveszítjük az otthonunkat), hirtelen semmit nem érnek az elveink, amelyekhez ragaszkodtunk stb.</p>
                         <p class="lead" style="text-align:justify;">Így hat az Uránusz a külső világban, és ezzel az a célja, hogy lelkileg is megérjünk. A testünk már felnőtt, de ez még nem jelenti azt, hogy lelki szinten is érettek és felnőttek vagyunk.</p>
                         <p class="lead" style="text-align:justify;">Az az időszak, amikor az Uránusz közbelép, a kiszolgáltatottság időszaka.</p>
                         <p class="lead" style="text-align:justify;">Ilyenkor a kötelékeink közé, amelyek a biztonságot jelentették számunkra, és amelyekhez életünk végéig ragaszkodni akartunk, belekeveredik egy – először idegennek tűnő – erő, amelynek hatására ezek a kötelékek megnyúlnak, és már nem jelentenek biztonságot. Ez az idegen erő könyörtelen: elveszítjük a talajt a lábunk alól.</p>
                         <p class="lead" style="text-align:justify;">Az ember ilyenkor aggódni kezd, mert észreveszi, hogy egy szakadékhoz ért. És minél jobban fél, annál nagyobb veszedelemnek érzi az előtte tátongó lyukat. Attól tart, hogy örökre eltűnik benne.</p>
                         <p class="lead" style="text-align:justify;">Viszont amikor abbahagyjuk a jajgatást, s felhagyunk az önsajnálattal, egy kissé felnőttebbé válunk. Ilyenkor azt mondjuk magunkban: “Látom az előttem tátongó szakadékot, és elfogadom, hogy ennek most így kell lennie. Nem próbálok meg visszakapaszkodni a meredek sziklafalon, annak semmi értelme; akkor háttal zuhannék tovább, még jobban félnék, s a végén úgyis leesnék a szakadék mélyére. Így hát, inkább repülni fogok!”</p>
                         <p class="lead" style="text-align:justify;">Az ember lenéz a szakadékba, széttárja a karját, és repül. Közben pedig egészen biztosan tudja, hogy egyszer újra földet ér. </p>
                         <p class="lead" style="text-align:justify;">A Plútó és az Uránusz jelenleg derékszöget alkotnak egymással. Ez tehát az újjászületés időszaka. Kiváltképp, ha személyes horoszkópunk is utal e két energia találkozására: pusztulás és feltámadás.</p>
                         <p class="lead" style="text-align:justify;">“Lapunkra tekintve láthatjuk, amint a Főnix, a lélek madara a levegőbe röppen. Keveseknek tűnik fel azonban, hogy alatta a világ romokban hever, minden porrá és hamuvá vált. A biológusok megállapítása szerint a hernyóban is megsemmisül minden sejt, mielőtt a pillangóban újjászerveződne. A hernyónak tehát épp úgy el kell pusztulnia, mint világunknak, mielőtt megvalósul a tényleges újjászületés.</p>
                         <p class="lead" style="text-align:justify;">Ez a pusztulás rendkívül fájdalmas – a szó szoros értelmében a halál megtapasztalását jelenti. Szenvedésre és fájdalomra mindig szükség van, mielőtt a lélek újra szárnyra kap. Időről időre mindannyian szeretnénk kitörni régi szokásainkból és körülményeinkből. Megújulásra vágyunk (természetesen egy szebb és jobb jövőre), de ha arról van szó, hogy fel kell adnunk érte régi formánkat, megijedünk, és megakadályozzuk a folyamatot. Szeretnénk megtartani régi házunkat, de egy újat is akarunk építeni. Metamorfózisra azonban nincs remény úgy, hogy közben a kecske is jóllakik, és a káposzta is megmarad. Új házat csak akkor építhetek életem alapjaira, ha a régit az utolsó tégláig lebontottam. Önként azonban senki sem bontja le létének védőbástyáját. Így mindig a sors feladata, hogy a születés csatornájába lökjön és megújulásra késztessen minket.</p>
                         <p class="lead" style="text-align:justify;">Az ember életében a legszörnyűbb a feltámadás előtti időszak. Tudatunk ragaszkodik a szerkezetekhez, a formákhoz. Az ego egyetlen túlélési lehetősége, ha ezekkel azonosítja magát. Plútó különböző életformákat vetít elénk, mi pedig hagyjuk magunkat rábeszélni. Így szilárdítjuk meg személyiségünket.</p>
                         <p class="lead" style="text-align:justify;">Ha most Uránusz, a felszabadító, szétrobbantja és megsemmisíti ezeket a formákat, elveszítjük ezt a támaszt, és ezzel – egy időre – szeretett énünket is. Ám e fájdalmas veszteség nélkül nem létezik fejlődés – és Plútó által fogva tartott lelkünk nem gyógyulhatna meg.”  (Peter Orban)</p>
