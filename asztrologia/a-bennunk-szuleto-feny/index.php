					     <div class="panel-body">
						 <br /> 
                         <p class="lead" style="text-align:justify;">2014. december 22-e több szempontból is különleges, ha az égbolton létrejövő változásokat figyeljük.</p>
                         <p class="lead" style="text-align:justify;">Először is újhold van, tehát a Nap és a Hold együttállása figyelhető meg, ráadásul ez az együttállás egy jegyváltáskor történik, hiszen épp ma hajnalban léptek át a Nyilasból a Bakba. Nemrég a Vénusz, majd a Merkúr is a Bak jegyébe lépett, a Plútó pedig már évek óta ott tartózkodik.</p>
                         <p class="lead" style="text-align:justify;">A másik, ami nagyon érdekes, hogy az Uránusz ma vet pontos quadrátot a Plútóra, ami együttáll a Vénusszal és a Merkúrral is, a Hold és a Nap pedig szintén efelé a quadrát felé közelít.</p>
                         <p class="lead" style="text-align:justify;">Mindehhez társul a Szaturnusz jegyváltása, hiszen a Skorpióban töltött időszaka lejárt, 23-tól már a Nyilasban vándorol.</p>
                         <p class="lead" style="text-align:justify;"><center><img class="img-responsive" style="off margin:21px auto 21px auto;" src="//cdn.sorskapu.hu/asztrologia-008-2.jpg" /></center></p>
                         <p class="lead" style="text-align:justify;">Sok-sok energia csoportosul tehát egy helyen, egy időben. Gazdag és változatos élményekben lehet részünk. Nagy nyomás nehezedik ránk, gátakkal, akadályokkal találkozunk. Nemcsak a mindennapokkal kapcsolatos teendőink során érezhetjük blokkolva magunkat, hanem érzelmi és gondolati síkon is. Kommunikációnk nehézkessé válhat. Mélyről jövő gátlásaink megakadályozhatnak minket abban, hogy tisztán és érthetően fejezzük ki magunkat. Érezzük, hogy valami nagy megkönnyebbülésre vágyunk, szeretnénk feloldani magunkban a hűvös ész és az áradó érzelmek kettősségéből adódó feszültséget. Szeretnénk minden félelemtől megszabadulni, minden múltból eredő sebet begyógyítani, minden arcunkra fagyott könnycseppet felolvasztani.</p>
                         <p class="lead" style="text-align:justify;">A földi lét és a látható világon túli birodalom között feszülő kötélen lépkedünk. A fizikai valóság terhe vállunkra nehezedve tol minket ide, a realitás keserű talajára, s közben húz a végtelen tér, melyben emlékeink szerint valamikor már otthonra találtunk. Igen, mindannyian tudjuk, hogy az, amit keresünk, nem itt található. Illetve itt is megtalálható, amennyiben létre tudjuk azt hozni. Nem egyszerű. Félelmetes, milyen emberek között vagyunk kénytelenek mozogni, miközben szorgalmasan végezzük hétköznapi kis feladatainkat. Egyre nehezebb elviselni azt a közeget, amit az utcára lépve szinte azonnal tapasztalunk. A szellemi korlátoltság gyakran megmutatkozik. Az érzelmi nyomor szintén. Tudatosságra kényszerít ez minket, akik szüntelenül kutatjuk értelmét ennek az egész értelmetlennek tűnő valaminek.</p>
                         <p class="lead" style="text-align:justify;">Mit keresek én itt, ilyen alakok és ilyen körülmények között? Hol van a saját kis helyem ebben a rohanó, ám folyamatosan változó világban? Hogyan tudnék felszabadulni a rengeteg kényszer alól, amit a társadalom, a politika, a gazdaság vagy akár néhány szűk közösség rám erőszakol?</p>
                         <p class="lead" style="text-align:justify;">Az én személyes válaszom az, hogy csakis belül találhatod meg a szabadságot, és csakis egy másik emberrel kapcsolatos élményen, tapasztaláson keresztül. A szabadság ugyanis nem más, mint az, amikor minden gátlás és akadály nélkül képes vagy átjutni a másik oldalra. Arra az oldalra, ahol könnyű vagy, mert egyetlen dologra tudsz csak figyelni: arra, hogy szeretsz. A végtelen tér, amiben feloldódsz egy-egy forrpontra hevült feszültség után, ugyanaz a hely, amit egy szoros, de gyengéd, meleg ölelésben találsz. Otthonra lelsz. Hirtelen átkerülsz abba a bizonyos másik világba.</p>
                         <p class="lead" style="text-align:justify;">Ezért azt tudom csak mondani, hogy jelen helyzetünkben mi, emberek, annyit tehetünk, hogy önmagunkban és közeli kapcsolatainkban kezdjük el keresni a megoldást. Ne hagyjuk magunkat letörni más emberek által, ne meneküljünk a hétköznapi kötelességeink elől, ne zárkózzunk el, ne essünk depresszióba, hanem forduljunk azok felé, akik legszűkebb környezetünkben élnek, azokhoz, akik hozzánk tartoznak! Értsük meg, hogy semmi sem lesz könnyebb addig, amíg velük nem találjuk meg a hangot! Mindannyian ugyanarra vágyunk: figyelemre, megértésre, együttérzésre és nyugalomra! Legyünk nyitottak egymás érzései és gondolatai felé! Öleljük meg egymást, és adjuk át magunkat a szeretet szüntelen áramlásának! Minél többen tesszük ezt, annál kisebb lesz a nyomás társadalmi szinten is. Ne vesszünk el az alacsony erkölccsel rendelkező emberek között, és ne is foglalkozzunk velük! Figyelmünket azokra irányítsuk, akiknek szükségük van ránk! Azon legyünk, hogy bármelyik pillanatban képesek legyünk visszatalálni ahhoz az önmagunkhoz, aki nem ítélkezik, nem sajnálja magát, nem panaszkodik, csupán ad és szeret. Felelősségünk elsősorban abban áll, hogy mire figyelünk, mire összpontosítunk, mire gondolunk, azaz hogy mennyire vagyunk tudatosak, illetve mennyire irányít minket tudatalattink ösztönös, fájdalommal és indulatokkal terhes, sötét közege.</p>
                         <p class="lead" style="text-align:justify;">Ezekben a napokban sokan kapjuk magunkat azon, hogy a halálon, az elmúláson gondolkodunk. Lehetnek halállal kapcsolatos élményeink, tapasztalataink is. De lehet, hogy elhunyt szeretteinkkel átélt emlékeink jönnek elő. Az Uránusz a halálfélelmünktől is szeretne most megszabadítani minket. Beleképzeltem magam a saját, eljövendő halálomba. Csodálatos volt. Óriási szeretet vett körül, s bennem is az volt. Így léptem át a határon, miközben a szeretet érzése egy folyamat volt, életen túl és halálon innen... Ez tette csodálatossá. És ez szüntette meg bennem a félelmet. Ekkor jöttem rá, hogy tulajdonképpen nem is a haláltól félünk, hanem attól, hogy hogyan halunk meg. Lesz-e bennünk elég szeretet, s lesz-e velünk valaki, aki átölel, s ezzel lehetővé teszi, hogy még itt, a Földön, a végtelen térben találjuk magunkat. Ez a hogyantól való félelem csak egyféleképpen oldható fel: arra kell rájönnünk és azt kell megtapasztalnunk, hogy hogyan kell élni! Hogyan kell úgy élni, mint aki éppen most haldoklik valaki szerető ölelésében!</p>
                         <p class="lead" style="text-align:justify;">Mindenkinek áldott karácsonyt kívánunk! Szeretetben gazdag pillanatokat, felszabadulást a félelmek alól, megkönnyebbülést a terhek súlya alól! Igazán szoros öleléseket, megértő, gyengéd, elfogadó, szerető tekinteteket! Úgy ajándékozzuk meg egymást, mintha Jézus születésnapjára vinnénk ajándékot, személyesen neki, a Megváltónak! Mert mindannyiunknak vannak személyes megváltói, akik segítenek cipelni keresztünket, végtelen szeretettel, önzetlenül, s akiken keresztül végül mi is megválthatjuk saját magunkat.</p>
