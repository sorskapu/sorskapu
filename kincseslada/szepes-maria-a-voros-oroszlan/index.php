                    <p class="lead" style="text-align:justify;">Szepes Máriával 1999-ben találkoztam először. Egyszerű, emeleti kis lakásában fogadott. S mivel látogatásomnak semmi konkrét célja nem volt azon kívül, hogy kifejezhessem tiszteletemet és csodálatomat, igencsak szerencsésnek éreztem magam azért, hogy ez a találkozó egyáltalán létrejöhetett.</p>
                    <p class="lead" style="text-align:justify;">Mária néni is ezzel kezdte. Elmondta, hogy már alig enged be otthonába valakit, nemrég még a televíziót is visszautasította. Idős már a hosszú beszélgetésekhez. De ha úgy alakul, hogy valaki mégis bent találja magát, annak oka van. Elárulta, hogy mi már sokszor találkoztunk, előző életeinkben. Dolgunk volt, s dolgunk van egymással. Hogy pontosan micsoda, arról nem beszélt. Talán maga sem tudta, s nem is akarta tudni. Talán egyszerűen csak érezte, és ez elég volt neki ahhoz, hogy közel engedjen magához. De az is lehet, hogy ő valóban emlékezett, s lelki szeme előtt valóban látta közös sorsunk részleteit. Nem tudom... de tulajdonképpen nem is lényeges.</p>
                    <p class="lead" style="text-align:justify;">Érdeklődött a horoszkópom felől, amelyet - mintha tudtam volna, hogy szükség lesz rá - magammal vittem. Házvezetőnőjét és tanítványát, Terikét kérte meg, hogy nézzen rá és olvasson belőle, majd maga is különféle utalásokat tett. Elmondta, hol tévedhetek el, ha nem vagyok elég bölcs, elmondta, mire figyeljek az utamon, s mi az, amiben tehetséges vagyok. Arra biztatott, hogy írjak, valamint arra, hogy továbbra is tanulmányozzam a karakterológiát - egészen alaposan - mert szükségem lesz rá. Nagyon figyeltem, és elhittem minden szavát. Bár csak 25 éves voltam, minden könyvét ismertem már, s ezért határtalan bizalommal fordultam felé. Egy sárga rózsát vittem neki ajándékba, ami miatt azonnal megszeretett. Elmesélte, hogy ez a leggyönyörűbb ajándék, amit csak kaphatott, mert férjére emlékezteti, Szepes Bélára, akit nagyon szeret. Ő vitt neki egyszer egy pontosan ugyanilyen sárga rózsát. Én el se tudtam volna képzelni, hogy ennyire örülni fog egyszerű kis ajándékomnak! Azt mondta, ez is egy jel számára: jól tette, amikor úgy döntött, hogy elmehetek hozzá.</p>
                    <p class="lead" style="text-align:justify;">Mária néni csodálatos lénye teljesen magával ragadott. Sosem láttam azelőtt embert így sugározni. Fényes volt, kedves, bölcs és igaz. Valódi Mester, aki nemcsak szavain, hanem tettein keresztül is tanított. Mária néni örök példakép marad számomra. Hálás vagyok a sorsnak, hogy megismerhettem.</p>
                    <p class="lead" style="text-align:justify;">7 évvel később kerestem meg újra, miután harmadjára is elolvastam A Vörös Oroszlánt. 18 évesen még nem értettem belőle szinte semmit, 25 éves koromban már sok mindenre felnyitotta a szemem, 32 évesen pedig egyenesen megváltásnak éreztem, hogy létezik ilyen könyv, aminek ráadásul egy dedikált példányát olvashatom. Sürgető vágy vett rajtam erőt: azonnal el kell kezdenem hangosan is felolvasni, hogy ez a remekmű minél több emberhez eljusson. S azért is, hogy - a hangsúlyozás előnyeit kihasználva - minél több ember számára váljon egyre érthetőbbé. Hiszen ez egy nagyon nehéz olvasmány. Magam is letettem az első 50 oldal után, mikor először a kezembe akadt. Csak azok képesek teljesen átrágni magukat rajta, akik sejtik, érzik, hogy a benne elrejtett felismerésekhez valóban hozzájuthatnak egyszer, amint eljön az ideje. És igen, egy bizonyos kor után, illetve egy bizonyos érettségi szinten már egyik felismerés jön a másik után! A Vörös Oroszlán egy olyan mű, melyet szinte kötelező többször is elolvasni, mert minden alkalommal egyre többet és többet ad. Emlékszem, az első találkozásunkkor örömmel meséltem Mária néninek, hogy én már kétszer is elolvastam A Vörös Oroszlánt, mire ő ezt válaszolta: “És még hányszor fogod!” Igaza lett.</p>
                    <p class="lead" style="text-align:justify;">Nagyon örült, amikor előálltam neki tervemmel. Izgatottan várta a hangoskönyvet. Majd amikor elkészült, azonnal végighallgatta. Elismerő szavaitól teljesen elérzékenyültem. Nem is reméltem, hogy ilyen erős hatást gyakorol majd rá. Innentől kezdve nap mint nap hallgatta, újra és újra, egészen élete végéig. Ica, aki másik nagy segítője volt, mesélte, hogy Mária néni néha széttárja a karját, s közben olykor-olykor felkiált: “Ez az!”, miközben hallgatja.</p>
                    <p class="lead" style="text-align:justify;">Leírhatatlan, amit ezzel kapcsolatban érzek... Egy belső hang által vezérelve megálmodtam, majd elkészítettem ezt a hangoskönyvet. Egészen pontos elképzeléseim voltak arról, milyennek kell lennie: színvonalas, tökéletes munkát akartam végezni. S minden jel arra mutatott, hogy ez sikerült is. Amikor magától a szerzőtől, Szepes Máriától hallottam ezeket a szavakat, éreztem, hogy valami nagy dolgot hoztam létre. Aztán Mária néni még tovább fokozta örömömet, ugyanis megkért arra, hogy a többi könyvét is olvassam fel. Következőnek a Raguel hét tanítványát, melyet legnagyobb műveként tartott számon. Sőt, kérte, hogy kiadatlan írásait is készítsem el hangoskönyv formájában. Egyet közülük rögtön a kezembe is adott. Közös volt tehát az öröm, a boldogság és a lelkesedés, s emiatt hihetetlen módon szárnyalni kezdett a lelkem. Mária néninek megígértem, hogy teljesítem a kívánságát, s azon leszek, hogy minél előbb elkezdhessem a Raguel felolvasását, melyet aztán követni fog az összes többi írása is.</p>
                    <p class="lead" style="text-align:justify;">A Vörös Oroszlánt megmutatta Novák Andrásnak, az Édesvíz Kiadó igazgatójának is, aki - miután betette autójának lejátszójába - megkeresett és gratulált. Személyes találkozásunkkor szó esett arról, hogy a hangoskönyv kiadásra kerül. Megismerkedtem Mária néni alapítványának elnökével, Almási Csabával is, aki szintén tetszését nyilvánította, s biztosított afelől, hogy mindenben számíthatok a segítségére. Így indult tehát A Vörös Oroszlán hangoskönyv változatának története, 2006-ban. S hogy aztán hogy alakult?</p>
                    <p class="lead" style="text-align:justify;">Egyik találkozónkon Mária néni azt mondta, készüljek fel rá, hogy a hangoskönyvnek ugyanaz lesz a sorsa, mint ami a könyvé volt. Tudjuk, hogy a könyvet bezúzták, meg akarták semmisíteni, örökre el akarták pusztítani. Az én munkámmal is ez történik majd. Tönkre akarják tenni, el akarják tüntetni, meg akarják akadályozni, hogy a hangom bárkihez is elérjen. De ne aggódjak, mert A Vörös Oroszlán elpusztíthatatlan, s ezért ez a csodálatos hangoskönyv is feltámad majd saját hamvaiból, újjáéled, újjászületik, mint a Főnixmadár. Hatalmas sikert jósolt neki, mert azt mondta, tökéletesen sikerült előadnom a művet. Szereplőit pontosan úgy jelenítem meg, ahogyan hangjuk az ő fejében szólt. Majd hozzátette, hogy amikor nem lesz már itt a Földön, felülről akkor is mindenben segíteni fog.</p>
                    <p class="lead" style="text-align:justify;">Az alkotás folyamatát rendkívüli módon élveztem. Szeretem a nehéz feladatokat, mert tűhegynyi pontba összesűrített koncentrációt igényelnek, és ez nagy kihívás. Folyamatos figyelemre van szükség; több síkon, párhuzamosan, egyszerre. Ha olvasás közben valami nem sikerült elsőre, annyiszor vettem fel újra, míg elégedetté nem váltam. Elképzeltem, hogy hallgatom, s úgy éreztem, nálam nagyon megzavarná az élményt, ha a lapozás neszei is átszűrődnének, ezért úgy döntöttem, ezt valahogy megoldom. Sokszor három-négy soros mondatokat kellett megjegyeznem ahhoz, hogy lapozás nélkül folytatni tudjam az olvasást. Előre oda kellett lapoznom ugyanis. De megérte a fáradozást, hiszen azt tapasztalom, hogy a hangoskönyv egy külön műfaj, ahol nem a tapintás, az érzékelés, a könyv illata, a papír simogatása és a lapozás öröme a fontos, hanem az a plusz, amit a hang és a beszéd - egy másik érzékszervünkön keresztül - a műhöz hozzáadni képes, s ezért jobb, ha ez a hang minél tisztább környezetben szól. Ez a bizonyos plusz tulajdonképpen nem más, mint maga a lélek. Miközben olvasok, a lelkemet adom a szöveghez, hogy az író szavainak közvetítése által eljussak a hallgatók lelkéhez. Amikor ez sikerül, úgy érzem, küldetésemet és célomat beteljesítettem.</p>
                    <p class="lead" style="text-align:justify;">Munkám elvégzéséhez minden segítséget megkaptam a sorstól. Biztosította a feltételeket számomra ahhoz, hogy tervemet megvalósíthassam. Az anyag elkészült. A problémák csak ezután következtek. Mária néni minden szava beteljesült. Egy - az elején támogatónak tűnő - segítőm ellenem fordult, s megmutatta, milyen az, amikor valaki keresztbe húzza a számításaimat. Sokat tanított ezzel nekem. Elsősorban azt, hogy minden esetben el kell tudni viselni, ha egy magasabb hatalom máshogy akarja irányítani a szálakat, mint ahogyan én. Biztosan kell tudnunk, hogy minden úgy jó, ahogy történik. Minden tapasztalás fejlődésünket szolgálja. Nagyban segítettek eljutni erre a szintre Mária néni biztató szavai: “Nyugodj meg, fel fog támadni, mint a Főnixmadár!”</p>
                    <p class="lead" style="text-align:justify;">Hosszú évek következtek a tetszhalál után. A téma fel-felbukkant bennem néha, majd ugyanolyan gyorsan el is illant. Tehetetlen voltam. A számító, pénzéhes energia, amivel szembetalálkoztam, megbénított ugyanis. De természetesen csak fizikai síkon. Hiszen valahol tudtam is és éreztem is, hogy egyszer valóban eljön az ideje a feltámadásnak. És így is lett.</p>
                    <p class="lead" style="text-align:justify;">A dolog úgy alakult, hogy egy ajándékon gondolkodtam a páromnak. Két feltételt szabtam: személyes legyen, és ha esetleg szükség van valami hozzávalóra, az ne kerüljön sokba. Mi legyen az, mi legyen az? - kerestem a választ. Néhány nap múlva eszembe jutott A Vörös Oroszlán. A hangoskönyv! Nagyon megörültem ennek az ötletnek, csak egy gondom akadt: a felvétel nem állt birtokomban. Annak idején ugyanis elvették tőlem. Még a másolatokat is. Csak pénzért kaphattam volna vissza, ráadásul nagyon sok pénzért cserébe, amivel pedig én nem rendelkeztem. Kénytelen voltam tehát lemondani róla. Lemondani verejtékes munkám gyümölcséről, és lemondani arról, hogy ezt a hanganyagot valaha is közkinccsé tehessem. Az ajándékozás sürgető vágya cselekvésre késztetett. Egyetlen másolati példány volt az országban, amihez hozzájuthattam: a Mária néni példánya. Megkerestem tehát az Alapítványt, és rövid határidővel kölcsönkértem azokat a lemezeket, melyeket ő hallgatott. Így kaptam vissza a kincsemet, ilyen mesébe illő módon.</p>
