                    <div class="panel-heading"><h2>Magunkról</h2></div>
                    <div class="panel-body">
                    <p class="lead" style="text-align:justify;">Szeretettel üdvözlünk mindenkit, aki megtisztel minket a figyelmével!</p>
                    <p class="lead" style="text-align:justify;">Úgy gondoltuk, ennyi kedves látogató után már illő  bemutatkoznunk, ezért most magamról, magunkról, a Sorskapuról és az asztrológiáról fogok beszélni egy kicsit.</p>
                    <p class="lead" style="text-align:justify;">A nevem Kerekes Andrea. Már több mint húsz éve foglalkozom asztrológiával, évekig tanultam kineziológiát, pszichológiát, mégsem tartom magam sem asztrológusnak, sem kineziológusnak, pszichológusnak pedig végképp nem, hiszen ebből a tárgyból végzettséget  sem szereztem. Célom soha nem az volt, hogy valamiféle szakmát tanuljak, miközben forgattam a jobbnál jobb e témákban íródott könyveket, hanem az, hogy minél inkább el tudjak igazodni saját kis világomban. Hogy negyven éves koromra mégis ennyire előtérbe került nálam az emberekkel való lelki munka, az a téma iránti elhivatottságomnak és sorsomnak köszönhető. Egyszerűen így alakult. Kinyílt előttem egy kapu, én pedig beléptem rajta.</p>
                    <p class="lead" style="text-align:justify;">Kislány korom óta az emberek és a köztük létrejövő kapcsolatok érdekelnek a legjobban. Emlékszem, hetedikes voltam, amikor kaptunk egy új, fiatal magyartanárnőt, aki arra kért minket, hogy mutatkozzunk be és mondjuk el, mivel töltjük a szabadidőnket, mivel szeretünk foglalkozni. Amikor rám került a sor, szó szerint ezt mondtam: engem az emberek érdekelnek. Persze az egész osztály röhögni kezdett, viszont a tanárnő szívébe rögtön beloptam magam. Később azt mondta a szüleimnek, hogy sose látott még ilyen bátor gyereket. Akkor még nem igazán értettem, mi ebben a bátorság, ma már tudom.</p>
                    <p class="lead" style="text-align:justify;">Aztán felnőttkoromra kiderült, hogy bizonyos szempontból valójában a gyáva emberek közé tartozom, és erről éppen az asztrológia vetette le a fátylat. Megtudtam ugyanis, hogy Mérleg az ascendensem. Azt pedig, hogy az osztálytársaim, majd később a munkatársaim gyakran kinevettek, csúfoltak és kiközösítettek, az ascendensemen álló Uránuszomnak köszönhetem. Ahogyan azt is, hogy fanatikus érdeklődésem ellenére képtelen voltam végigjárni egy négy éves asztrológiai iskolát. Megpróbáltam ugyan, de hamar rá kellett jönnöm, hogy az a rendszer, ami ott működik, nem nekem való. Én a saját tempómban szeretek haladni, ezért rendkívül zavart, hogy éveket kellett volna várnom, mire előadták volna nekem azokat a dolgokat, melyek a legjobban érdekeltek. Unatkoztam az órákon, ezért négy-öt alkalom után abba is hagytam az egészet. Hazasiettem és magam tanulmányoztam tovább - akkor éppen - Friedrich Schwickert csodálatos könyveit. A horoszkópok kiszámításában és megrajzolásában érzéki örömöt leltem, szerelmes voltam az asztrológiába, s ez a szerelem mind a mai napig él. Gondolom, ebből rájöttetek, hogy a Napom a Bika jegyében áll.</p>
                    <p class="lead" style="text-align:justify;">Annak ellenére, amiket az előbb elmeséltem, számomra is elhozta az Isten a mestert, a nagy tanítót, Dr. Peter Orban személyében. Semmi kétségem nincs afelől, hogy kettőnk találkozása sorsszerű volt. Ahogyan váratlanul belépett az életembe, azt úgy éltem meg, mintha csoda történt volna. S hogy később lehetőségem nyílt arra, hogy személyesen is tanulhassak tőle, az egyszerűen minden elképzelésemet meghaladta. Fantasztikus ember ő, ami már írásain keresztül is teljesen átjött nekem, de az évek során meg is győződhettem arról, hogy ez valóban így van. Csoporttársaim közül nem mindenki volt ilyen elégedett vele, de én megtaláltam benne azt a tapasztalt, bölcs, öreg és örök barátot, akire mindig is vágytam. Hála Istennek, ez a barátság kölcsönös. Átsegített életem nehéz időszakain, miközben folyamatosan tanított, anélkül, hogy tanítani akart volna, s anélkül, hogy valaha is beleavatkozott volna az életembe. Támogatását mind a mai napig élvezem, és ezért rendkívül  hálás vagyok. Az elmúlt években létrehoztunk néhány közös munkát, s a jövőre nézve is tervezek még sok mindent, ami vele kapcsolatos. Idén töltötte be 70. életévét, Isten éltesse, remélem, sokáig velünk marad!</p>
                    <p class="lead" style="text-align:justify;">Visszatérve az asztrológiára, szeretném elmondani, hogy én a bolygókat különféle szerepszemélyiségekként kezelem. Mindegyikük bennünk él, a lelkünkben. S hogy melyikük kerül előtérbe, illetve melyiket fojtjuk el, az egy horoszkópból azonnal kiderül. Épp úgy, ahogy az univerzumban, lelkünkben is folyamatosan mozog és változik minden. Én az asztrológiát ennek a változásnak az elősegítésére használom. A horoszkóp számomra isteni segédeszköz ahhoz, hogy kapcsolódni tudjak a hozzám forduló emberekhez. Ezen kívül megspórolja nekem az időt, mert egyetlen szimpla beszélgetés sem képes annyi mindenre rávilágítani, mint egy születési képlet. Egy horoszkóp megmutatja nekem a másik mélyen gyökerező félelmeit, megmutatja, miben tehetséges, elárulja, mire vágyik, milyen hajlamokkal jött a világra, milyen berögződéseket táplál, s mi az, ami leginkább hiányzik neki ahhoz, hogy boldog lehessen.</p>
                    <p class="lead" style="text-align:justify;">Sokszor észre sem vesszük, hogy nem mi irányítjuk az életünket, hanem a tudatalattink. Ösztönösen reagálunk a dolgokra, személyiségünk alapvető jellemvonásain keresztül. Nem számolunk a következményekkel. Mindaddig, amíg a külső körülmények rá nem kényszerítenek minket. Most gondolj magadra! Ott állsz életed egy bizonyos pontján. A jelenben. Rád milyen kényszerítő erők hatnak? Mi az, ami gátol a fejlődésben, előrehaladásban? Életed melyik területén találkozol a legtöbb akadállyal? Mi az, amit nagyon akarsz, mégse sikerül?</p>
                    <p class="lead" style="text-align:justify;">Sorolhatnám még a kérdéseket, de egy biztos: a végső válasz minden esetben a tudatalattidban rejlik. Ott rejtőzködik és arra vár, hogy észre vedd. Azt szeretné, hogy foglalkozz vele, törődj vele. Ha ezt megteszed, megjutalmaz téged: teljes valójában megmutatkozik előtted, és ezzel rendelkezésedre bocsátja azt az energiát, mellyel aztán sikerül továbblépned. Ez azt is jelenti, hogy a munkát minden esetben te végzed. Én csupán segítőd lehetek ezen az úton. Ha úgy érzed, elfáradtál, elakadtál, eltévedtél vagy egyszerűen csak szeretnéd jobban megismerni magad, én szívesen melléd szegődöm egy rövid időre, és legjobb tudásom szerint támogatlak téged. Megfogom a kezed és elkísérlek egy darabon, hogy aztán tágabb tudattal, nyugodtabb lélekkel és tisztább gondolatokkal mehess tovább a saját utadon.</p>
                    <p class="lead" style="text-align:justify;">Fontos azt is megemlítenem, hogy mivel én Peter Orbantól nemcsak asztrológiát tanultam, hanem családállítást vezetni is, nálam ez a bizonyos hellingeri filozófia teljesen beépült a horoszkóp értelmezésének folyamatába. Amikor egy radixra nézek, azonnal szemet szúrnak a családi összefonódások, a régmúltból hozott minták, látom, hol vannak azok a területek, ahol minél előbb vissza kell állítani az egyensúlyt, létre kell hozni a harmóniát. Tulajdonképpen én ebben látom az asztrológia lényegét. Ahogy minden más dolog lényegét is ezen a világon. Az egyensúly, a harmónia, a nyugalom és a béke - ezek az iránymutatóim. Éppen ezért ezek létrejöttéért fáradozom, hűen Mérleg ascendensemhez. Nyilván az élet ad is ehhez kellő mennyiségű gyakorlópályát, ami azt jelenti, hogy magam is rengeteg nehézségen mentem már keresztül. Sorsom nem kímélt meg engem a különféle csapásoktól, a nehéz és kilátástalannak tűnő helyzetektől, s a szinte kibírhatatlanná duzzadt  konfliktusoktól. De tudom, hogy mindez nem véletlenül történt így. S azt is tudom már, hogy meg kellett tanulnom irányítani a sorsomat, már amennyire ez lehetséges. Elfogadni a dolgokat úgy, ahogy azok vannak, mindenképpen célravezető, sőt, mindig ez az első lépés. A következő pedig leginkább tudatosságomon múlik. Ezért nekem ennyi elég, jöhet bármi. Az élet számomra arról szól, hogy megtanuljam kezelni a múlt következményeit, azaz a jelen pillanatban megnyilvánuló helyzeteket.</p>
                    <p class="lead" style="text-align:justify;">Az, hogy a sok nehézség ellenére sikerült megnyílnom a világ felé, páromnak köszönhető. Lelki életem alakulásának legintenzívebb élményeit vele élem át. S aztán vele beszélem meg azokat a lelki folyamatokat, melyeken keresztülmentem, s melyeken ő keresztülment. Sokat tanulunk egymástól, ahogy annak lennie kell. András a támaszom, aki segít nekem megvalósítani az álmaimat, s ezzel szerintem a legtöbbet adja, amit egy férfi adhat egy nőnek, akit szeret. Még azt is megteszi, ha kérem, hogy a hangját kölcsönzi a képzeletemben megjelenő szerepszemélyiségek ábrázolásához, ami egyáltalán nem egyszerű feladat. Mondom ezt 18 évnyi szinkronizálás után, hiszen ami a foglalkozásomat illeti, már jó ideje ezt csinálom. Sok-sok filmszerepben gyakorolhattam és gyakorolhatom, milyen az, mikor a hangom segítségével, a beszéden vagy az éneklésen keresztül kell megformálnom és átadnom különféle érzelmeket.</p>
                    <p class="lead" style="text-align:justify;">Így alakult tehát, hogy 2011-ben megszületett a Sorskapu, melynek születése után három éven keresztül más feladata nem volt, minthogy növekedjen és erősödjön bennünk csendesen, s mely idén augusztusban végre saját útjára indult. Mind a ketten bízunk benne, hogy ez az út eredményes lesz, sikeres, változatos és lélekemelő, s bízunk abban is, hogy végigkísér minket az egész életen. Sőt, még azon is túl!</p>
