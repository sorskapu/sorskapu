                    <div class="panel-body">
                    <p class="lead" style="text-align:justify;">Arra a kérdésre keressük a választ, hogy ki hiányzik. Konkrétan:</p>
					<h3 style="text-align:left;">1. Hol van az az ember, akinek nincs helye a családunkban?</h3>
					<p class="lead" style="text-align:justify;">Nincs helye, mert annak idején valami olyasmit tett, amit a család többi tagja szégyellt, s ahelyett, hogy vállalták volna érte a felelősséget, kirekesztették, kitaszították a családból. <br />Ilyenek például az öngyilkosok, akiket még csak el sem temettethettek. Vagy a régebbi időkben ilyenek voltak a homoszexuálisok, akiket börtönbe zártak, majd akár agyon is vertek. Fontos, hogy megtaláljuk őket, mert ellenkező esetben könnyen előfordulhat, hogy egy-két generációval később születik valaki, aki egyszer csak arra ébred, hogy homoszexuális, majd nem érti, miért, de rendszeresen veszélyes helyeken találja magát, olyan helyeken, ahol utálják a homoszexuálisokat, s bántják is őket. Ha a régi rokon újra a család emlékezetébe kerül (pl. egy állítás során szó esik róla), s megkapja az őt megillető helyet, ami mindenkinek jár egy családban, nem lesz szükség arra, hogy a leszármazott például ugyanúgy gyilkosság áldozata legyen. Vagy ha azt, aki egykor öngyilkosságot követett el, egy állítás során szeretettel visszafogadják a családba, és érezheti a lelke, hogy végre nem kirekesztett, hanem őt is elfogadták, szörnyű tette ellenére, akkor nem lesz szükség rá, hogy később egy leszármazottja szintén öngyilkosságot kövessen el.</p>
                    <h3 style="text-align:left;">2. Hol van az az ember, akinek nincs jó helye a családunkban?</h3>
					<p class="lead" style="text-align:justify;">Nincs jó helye, mert haragszunk rá, dühöt és gyűlöletet érzünk iránta, képtelenek vagyunk arra, hogy bármiért is köszönetet mondjunk neki. <br />Ide tartozhatnak például a volt feleségek, volt férjek, testvérek, szülők, közeli hozzátartozók, akiknek nem ismerjük el a jelentőségét. Nem az a fontos, hogy a konkrét személy, akiről szó van (pl. volt feleség) hogyan érzi magát a világban, hanem az, hogy hogyan érzi magát az én lelkemben. Ameddig képtelen vagyok felismerni, hogy a másik ember mire tanított, mit tapasztalhattam meg a segítségével, miért lehetek hálás neki (pl. világra hozta a gyermekeimet), addig esélyem sincs arra, hogy egy új párkapcsolatban boldog lehessek. Másik példa, ha valaki lenézi a nála idősebb testvért vagy akár a saját apját, mert szerinte nem sokra vitte. Azt hiszi, ő többet ér nála. Majd egy idő után megbetegszik, esetleg elkezd fájni a háta. Addig görnyed majd egyre mélyebbre, míg fel nem ismeri, hogy ilyen felfogással nem boldogulhat az életben.</p>
					<h3 style="text-align:left;">3. Hol van az az ember, akinek nincs megfelelő helye a családunkban?</h3>
					<p class="lead" style="text-align:justify;">Nincs megfelelő helye, mert nem a szerepének megfelelően kezelik. <br />Például születik egy nőnek egy kisfia, de úgy, hogy a gyermek apja már házasságban él egy másik nővel, s nem hagyja el a családját, még az új gyermek kedvéért sem. Ebben az esetben könnyen előfordulhat, hogy az anya nem a kisfát látja, hanem a szerelmét. Úgy viselkedik a gyerekével, mintha a szerelme lenne. Ezzel megnehezíti gyermeke dolgát, hiszen ha ő foglalja el a leendő feleség helyét, nem lesz nő, aki a fiát boldoggá tudja tenni majd. A fiúnak senkire sem lesz igazán szüksége. Még akkor sem, ha a látszat mást mutat, azaz családapa és férj lesz maga is. Ha a viszonyok nem kerülnek helyre, nem valószínű, hogy valaha is boldog házassága lesz.</p>
					<h3 style="text-align:left;">4. Vannak különleges esetek is, melyek az előző három ponthoz nem sorolhatók.</h3>
					<p class="lead" style="text-align:justify;">Ilyen például az, amikor a kliens saját magával találkozik. Mondjuk kapcsolatba lép 4 éves kori önmagával, mert annak idején leszakadt a személyiségének ez a része. Ilyenkor vissza kell fogadnom magamba ezt a részt, be kell építenem jelenlegi, felnőtt személyiségembe. Vagy előkerül a születésem témája, és úgy alakul, hogy újra át kell élnem az egészet, csak most egy állítás keretein belül. Annak érdekében tesszük mindezt, hogy teljesebbé, egész-ségesebbé váljunk, a szó minden értelmében.</p>
