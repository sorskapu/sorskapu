                    <div class="panel-body">
                    <p class="lead" style="text-align:justify;">Döbbenetes és szinte hihetetlen, ahogy egymás számára idegen emberek pontosan megjelenítik egymás lelki folyamatait, testi érzékeléseit (pl. amikor egy ember képviselője hirtelen nem érzi a lábát, holott nem is tudja, hogy az adott ember, akit képvisel, elveszítette azt), gondolatait (szó szerint idézik néha a képviselt embert). Mivel saját szememmel győződtem meg ezekről, sok-sok állítás során, számomra a módszer abszolút hiteles és elfogadható.</p>
