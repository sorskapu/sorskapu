                    <div class="panel-body">
                    <p class="lead" style="text-align:justify;">Szerintem minden módszerre igaz, így a családfelállításra is, hogy bár történik előrehaladás, a kliens akkor lehet csak biztos abban, hogy minden megfelelően halad az életében, ha nem hagy fel személyiségének folyamatos csiszolásával. Erre is rengeteg módszer van már, én magam az asztrológiát használom előszeretettel. Szívesen fogadok bárkit, aki jellemén szeretne munkálkodni. Ezt sohasem késő elkezdeni, és sohasem érdemes abbahagyni.</p>
