					<div class="panel panel-default">
                    <div class="panel-heading"><h2>Milyen témák esetében használható a családfelállítás? Mi az, amiben segít?</h2></div>
                    <div class="panel-body">
                    <p class="lead" style="text-align:justify;">Mivel majd' minden betegség onnan ered, hogy lélekben nem vagyunk kerek egészek, kapcsolataink nem szeretettel teliek, személyiségünk és jellemünk nincs megfelelően kimunkálva, nem rendelkezünk kellő önismerettel, nem vagyunk tisztában tudatalatti mozgatórugóinkkal, ezért bármilyen betegség esetén végezhetünk családállítást.</p>
                    <p class="lead" style="text-align:justify;">Az előző mondat részleteiben megbújik, hogy tulajdonképpen minden témára lehet állítani. Lelki problémákra, életvezetési problémákra, párkapcsolati -, kapcsolati problémákra, munkahelyi-, közösségi problémákra stb.</p>
					<p class="lead" style="text-align:justify;">De még csak az sem szükséges, hogy problémánk legyen! Önismereti munkának is megfelelő ez a módszer. Minden egyes állítás során új képeket kapunk ahhoz, hogy jobban eligazodjunk saját kis világunkban.</p>
					</div>
                    </div>
					
					<div class="panel panel-default">
                    <div class="panel-heading"><h2>Szokásos félelmek az állítással kapcsolatban:</h2></div>
                    <div class="panel-body">
                    <p class="lead" style="text-align:justify;">Azért beszélek erről, mert sok embert ismerek, aki nagyon szeretne részt venni egy állításon, de nem mer. Nekik mindenek előtt azt ajánlom, hogy képviselőként vegyenek részt a csoportban, ami azt jelenti, hogy saját állítást nem kérnek, de ugyanúgy megtapasztalhatják a családállítás által nyújtott élményeket.</p>
					<p class="lead" style="text-align:justify;">A leggyakrabban előforduló félelmek:</p>
					<p class="lead" style="text-align:justify;">1. Fél attól, hogy idegen emberek előtt kell kiadnia magát. Ez jogos félelem. Nem tudhatjuk, ki és mit használ majd fel ellenünk a későbbiek során. Magam is megtapasztaltam, milyen nehéz, amikor olyan emberek adnak ki rólam belső információkat, akiktől nem vártam volna. Mégis azt tudom mondani, hogy bátran vállalni kell! Ha nem tudunk nyíltan beszélni magunkról, akkor nem adunk lehetőséget arra, hogy bármilyen változás bekövetkezhessen. Ha valaki ennek ellenére mégsem vállalja a csoportos munkát, annak az egyéni állítást javaslom, mely szintén eredményes, de nem olyan intenzív, mint egy csoportban átélt családfelállítás.</p>
					<p class="lead" style="text-align:justify;">2. Fél attól, hogy rajta már úgysem lehet segíteni. Ez számomra elképzelhetetlen. Csak az gondolkodhat így, aki nem bízik saját magában, nem bízik saját erejében, nem tudja elhinni, hogy talán csupán egy kis segítségre van szüksége (ami nem szégyen, mindannyiunkkal előfordul!), és lélekben megerősödve képes lesz ő is létrehozni a saját csodáját.</p>
					<p class="lead" style="text-align:justify;">3. Fél attól, hogy olyan állapotba kerül, amikor már nem képes uralkodni az érzelmei fölött, amikor már nem tudja kontrollálni magát. Mindenkit megnyugtatok, hogy az én praxisomban ilyen sosem fordulhat elő. Bár a mező hatására kialakul egy sajátos tudatállapot, ami nem hasonlítható a hétköznapi tudatállapotunkhoz, mégsem veszítjük el a kontrollt.</p>
					<p class="lead" style="text-align:justify;">4. Fél attól, hogy képviselőként nem fogja tudni érzékelni a képviselt embert. Fél, hogy ő majd nem érez semmit. Amennyiben ez megtörténik, annak jelentősége lesz, s be fog épülni a munkánkba. Nem érdemes ettől félni! Ki kell próbálni a helyzetet, s meg kell tapasztalni!</p>
