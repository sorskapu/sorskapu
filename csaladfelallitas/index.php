                    <div class="panel-body">
                    <p class="lead" style="text-align:justify;">A módszer alapjául az ún. morfogenetikus mező szolgál, melynek lényege, hogy létezik egy olyan energetikai közeg, melyben minden valaha átélt eseménynek és gondolatnak nyoma van, s melyben ezek az információk ide-oda közlekednek, a tér és az idő korlátai nélkül. Tulajdonképpen telepatikus úton hatunk egymásra, anélkül, hogy ennek tudatában lennénk. Igazán még senki sem tudta megmagyarázni, pontosan hogyan működik ez, viszont tapasztalati tény, hogy működik. Több tudós is vizsgálta, s vizsgálja mind a mai napig ezt a mezőt. Közülük talán Sheldrake neve hangzik a legismertebben azok számára, akik foglalkoztak már ezzel a témával. Ő használta saját elméletéhez ezt a kifejezést: formáló, azaz morfogenetikai mező, melyet mi szellemi mezőnek nevezünk a családállítások során. Ez a szellemi mező nemcsak tárolja az információkat (érzelmek, gondolatok, emlékek), hanem tovább is adja azokat, ezért hívhatnánk akár beszélő mezőnek is.</p>
